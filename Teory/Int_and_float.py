# Числа. Integer и Float 

#Integer (int)
i = 10 
print(i)

#Float 
f = 3.14
print(f)

#Математические операции с числами
a = 7
b = 3
#Сложение
sums = a+b
print(sums)

#Вычитание
dif = a-b 
print(dif)

#Умножение
mult = a*b
print(mult)

#Деление
div = a/b
print(div)

#Деление без остатка
mdiv = a//b
print(mdiv)

#Остаток от деления
rem = a % b
print(rem)

#Возведение в степень
sqrt = a**b
print(sqrt)

#Преобразование float в int
